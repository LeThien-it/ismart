    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\User  $user
     * @param  \App\Role  $role
     * @return mixed
     */
    public function view(User $user, Role $roleMain)
    {
        $permissions = 0;
        foreach ($user->roles as $role) {
            $permissions += count($role->permissions);
        }
        // dd($permissions);
        if (($user->checkPermissionAccess('list_role') && $roleMain->user_id == $user->id) || $permissions >= 32) {
            return true;
        }
        return false;
    }

